          <h3>อำเภอต่าง ๆ ในจังหวัดตาก</h3>
          <h5>
            1.อำเภอเมืองตาก
          </h5>
          <p class="text-justify">
            อำเภอเมืองตาก เป็นอำเภอในจังหวัดตาก ศูนย์กลางทางด้านคมนาคม และจุดเชื่อมต่อไปยังอำเภอแม่สอด ศูนย์กลางค้าชายแดนไทย-พม่า อำเภอเมืองตากเดิมชื่อ "เมืองระแหง" อดีตมีชาวมอญอาศัยอยู่มาก่อน ดังมีหลักฐานศิลปะมอญปรากฏอยู่ ตัวเมืองเดิมตั้งอยู่ที่อำเภอบ้านตาก เมืองนี้สร้างขึ้นก่อนสมัยกรุงสุโขทัยเป็นราชธานี ต่อมาเมื่อได้สถาปนากรุงสุโขทัยเป็นราชธานีแล้ว เมืองตากมีฐานะเป็นเมืองหน้าด่านที่สำคัญด้านฝั่งตะวันตก ในสมัยกรุงรัตนโกสินทร์ พระบาทสมเด็จพระพุทธเลิศหล้านภาลัยได้โปรดเกล้าฯ ให้ย้ายตัวเมืองตากจากฝั่งขวาของแม่น้ำปิงมายังฝั่งซ้ายบริเวณตำบลบ้านระแหงจนกระทั่งทุกวันนี้
          </p>
          <h5>
            2.อำเภอบ้านตาก
          </h5>
          <p class="text-justify">
            อำเภอบ้านตาก สันนิษฐานว่าคือตัวเมืองตากเดิม เป็นเมืองหน้าด่านสำหรับป้องกันข้าศึกศัตรูมาแต่ดึกดำบรรพ์ ตั้งอยู่บนดอยเล็กๆ ลูกหนึ่ง ปัจจุบันคือบ้านท่าพระธาตุ หมู่ที่ 3 ตำบลเกาะตะเภา อยู่ทางทิศตะวันตกของแม่น้ำปิง ต่อมาสมัยสมเด็จพระมหาธรรมราชา แห่งกรุงศรีอยุธยา ได้ย้ายเมืองตากจากที่เดิมมาอยู่ที่ตำบลป่ามะม่วง อำเภอเมืองตาก และในสมัยพระบาทสมเด็จพระพุทธเลิศล้านภาลัย แห่งกรุงรัตนโกสินทร์ ได้ย้ายเมืองตากไปอยู่ที่ตำบลระแหง อำเภอเมืองตาก ฝั่งตะวันออกของแม่น้ำปิง ซึ่งเป็นที่ตั้งตัวจังหวัดตากในปัจจุบันนี้
          </p>
