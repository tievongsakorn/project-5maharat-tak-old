              ท่าสองยาง เป็นอำเภอในจังหวัดตาก ติดชายแดนประเทศพม่า ริมแม่น้ำเมย พื้นที่ส่วนใหญ่เป็นป่าเขาสูงตามแนวทิวเขาถนนธงชัย ประชากรส่วนใหญ่เป็นชาวไทยพื้นราบ ประกอบอาชีพทำไร่ ระยะทางจากอำเภอแม่สอดถึงอำเภอท่าสองยาง ประมาณ 84 กิโลเมตร รวมระยะทางจากตัวอำเภอเมืองตากถึงอำเภอท่าสองยางประมาณ 169 กิโลเมตร
            </p>
            <h5>
              6.อำเภอแม่สอด
            </h5>
            <p class="text-justify">
              แม่สอด (คำเมือง: Lanna-Mae Sot.png; พม่า: မဲဆောက်) เป็นอำเภอหนึ่งทางตอนกลางของจังหวัดตาก ได้รับการจัดตั้งเป็นอำเภอมาตั้งแต่ พ.ศ. 2441 ตัวอำเภออยู่ในที่ราบระหว่างภูเขาระหว่างเทือกเขาถนนธงชัยทิวเขาในฝั่งประเทศไทย อีกส่วนหนึ่งเป็นทิวเขาฝั่งประเทศพม่า อำเภอแม่สอดมีพื้นที่ประมาณ 1,986 ตารางกิโลเมตร ประชากรมีทั้งชาวเขาและคนที่อพยพจากอำเภอเมืองตากเข้าไปตั้งถิ่นฐานอยู่ รวมทั้งชาวพม่าที่มีภรรยาและบุตรเป็นคนไทยด้วย อำเภอแม่สอดอยู่ห่างจากอำเภอเมืองตาก 86 กิโลเมตร
            </p>
            <h5>
              7.อำเภอพบพระ
            </h5>
            <p class="text-justify">
              อำเภอพบพระ เป็นอำเภอหนึ่งของจังหวัดตาก ความสำคัญของอำเภอพบพระในทางภูมิศาสตร์ที่สำคัญก็คือมันเป็นอำเภอที่เป็นต้นกำเนิดของแม่น้ำเมย แม่น้ำพรมแดนสายสำคัญระหว่างไทยกับพม่า
            </p>
            <h5>
              8.อำเภออุ้มผาง
            </h5>
            <p class="text-justify">
              อำเภออุ้มผาง เป็นอำเภอหนึ่งทางตอนใต้ของจังหวัดตาก จัดตั้งขึ้นตามพระราชกฤษฎีกาตั้งอำเภออุ้มผาง พ.ศ. 2502 อันมีผลใช้บังคับตั้งแต่วันที่ 6 พฤษภาคม พ.ศ. 2502 อำเภอนี้ที่มีขนาดพื้นที่มากที่สุดและเป็นอำเภอที่อยู่ห่างไกลจากตัวอำเภอเมืองมากที่สุดในประเทศไทย
            </p>
