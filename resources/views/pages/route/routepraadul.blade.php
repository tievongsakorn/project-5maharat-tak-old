            <p>พระบาทสมเด็จพระบรมชนกาธิเบศร มหาภูมิพลอดุลยเดชมหาราช บรมนาถบพิตร (5 ธันวาคม พ.ศ. 2470 – 13 ตุลาคม พ.ศ. 2559) หรือ พระบาทสมเด็จพระปรมินทรมหาภูมิพลอดุลยเดช เป็นพระมหากษัตริย์ไทย รัชกาลที่ 9 แห่งราชวงศ์จักรี ครองราชย์ตั้งแต่วันที่ 9 มิถุนายน พ.ศ. 2489 นับเป็นพระมหากษัตริย์ที่ทรงครองสิริราชสมบัติยาวนานที่สุดในประเทศไทยและเอเชียตะวันออกเฉียงใต้

              พระองค์เสด็จพระราชสมภพเมื่อวันที่ 5 ธันวาคม พ.ศ. 2470 ที่โรงพยาบาลเมาต์ออเบิร์น เมืองเคมบริดจ์ รัฐแมสซาชูเซตส์ สหรัฐ เป็นพระราชโอรสในสมเด็จพระมหิตลาธิเบศร อดุลยเดชวิกรม พระบรมราชชนก กับสมเด็จพระศรีนครินทราบรมราชชนนี และเป็นพระราชนัดดาในพระบาทสมเด็จพระจุลจอมเกล้าเจ้าอยู่หัว กับสมเด็จพระศรีสวรินทิราบรมราชเทวี พระพันวัสสาอัยยิกาเจ้า
              
              พระองค์เป็นพระมหากษัตริย์ภายใต้รัฐธรรมนูญ และได้ทรงหยุดยั้งการกบฏ เช่น กบฏเมษาฮาวาย ในปี พ.ศ. 2524 และ กบฏทหารนอกราชการ ในปี พ.ศ. 2528 กระนั้น ในสมัยของพระองค์ได้มีการทำรัฐประหารโดยทหารหลายคณะ เช่น จอมพลสฤษดิ์ ธนะรัชต์ ในปี พ.ศ. 2500 กับพลเอกสนธิ บุญยรัตกลิน ในปี พ.ศ. 2549 ตลอดระยะเวลา 70 ปี 4 เดือน 4 วัน ที่ทรงครองราชย์ เกิดรัฐประหารโดยกองทัพ 11 ครั้ง รัฐธรรมนูญ 16 ฉบับ และนายกรัฐมนตรี 27 คน
              
              ประชาชนชาวไทยส่วนใหญ่เคารพพระองค์ อนึ่ง ตามรัฐธรรมนูญ พระมหากษัตริย์ทรงอยู่ในฐานะอันเป็นที่เคารพสักการะ และผู้ใดจะละเมิดมิได้ ส่วนประมวลกฎหมายอาญาบัญญัติไว้ว่า การดูหมิ่น หมิ่นประมาท หรืออาฆาตมาดร้ายพระมหากษัตริย์เป็นความผิดอาญาคณะรัฐมนตรีหลายชุดที่ได้รับการเลือกตั้งมาก็ถูกคณะทหารล้มล้างไปด้วยข้อกล่าวหาว่านักการเมืองผู้ใหญ่หมิ่นพระบรมเดชานุภาพกระนั้น พระองค์เองได้ตรัสเมื่อปี พ.ศ. 2548 ว่า "สาธารณชนพึงวิพากษ์วิจารณ์พระองค์ได้"
              
              พระองค์ทรงเป็นที่สรรเสริญในประเทศไทยเกี่ยวกับพระราชดำริในเรื่องปรัชญาเศรษฐกิจพอเพียง โคฟี แอนนัน เลขาธิการสหประชาชาติ ได้ถวายรางวัลความสำเร็จสูงสุดด้านการพัฒนามนุษย์แด่พระองค์ด้านสินทรัพย์ของพระองค์นิตยสารฟอบส์จัดอันดับให้พระองค์เป็นพระมหากษัตริย์ผู้มีพระราชทรัพย์มากที่สุดในโลก ตั้งแต่ปี พ.ศ. 2551 ถึง พ.ศ. 2556 เมื่อเดือนพฤษภาคม พ.ศ. 2557 พระองค์มีพระราชทรัพย์ 30,000 ล้านดอลล่าร์สหรัฐ (ดูหมายเหตุด้านล่าง)สำนักงานทรัพย์สินส่วนพระมหากษัตริย์นั้นใช้สินทรัพย์เพื่อสวัสดิการสาธารณะ เช่น เพื่อพัฒนาเยาวชน แต่ได้รับการยกเว้นมิต้องจ่ายภาษีและให้เปิดเผยการเงินต่อพระมหากษัตริย์แต่พระองค์เดียวขณะที่พระบาทสมเด็จพระปรมินทรมหาภูมิพลอดุลยเดชก็ได้ทรงอุทิศพระราชทรัพย์ไปในโครงการพัฒนาประเทศไทยหลายต่อหลายโครงการ โดยเฉพาะในทางเกษตรกรรม สิ่งแวดล้อม สาธารณสุข การส่งเสริมอาชีพ ทรัพยากรน้ำ สวัสดิการทางคมนาคม และสวัสดิการสาธารณะ
              
